'use strict';

module.exports = {
    get: function() {
        return browser.get('/my-module');
    },
    pageHeader: function() {
        return element(by.css('.page-header')).getText();
    },
    pageTitle: function() {
        return browser.getTitle();
    },
    breadscrumb: function() {
        return {
            first: function() {
                return element(by.css('.breadcrumb li:nth-child(1) > a'));
            },
            second: function() {
                return element(by.css('.breadcrumb li:nth-child(2) > a'));
            },
            third: function() {
                return element(by.css('.breadcrumb li:nth-child(3)'));
            }
        }
    }
};